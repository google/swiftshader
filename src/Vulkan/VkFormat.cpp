		case VK_FORMAT_ASTC_8x5_SRGB_BLOCK:
		case VK_FORMAT_ASTC_8x5_SFLOAT_BLOCK_EXT:
		case VK_FORMAT_ASTC_8x6_UNORM_BLOCK:
